// Push e Pop:
// Crie um programa que simule uma pilha (stack) de pratos.
// Use o método push para adicionar pratos e o método pop para retirar pratos.

const stack = ["Plate 1", "Plate 2", "Plate 3"];
stack.push("Plate 1");
stack.push("Plate 2");
stack.push("Plate 3");

stack.pop();
console.log(stack);

// Esperado:
// ["Plate 1", "Plate 2"]
